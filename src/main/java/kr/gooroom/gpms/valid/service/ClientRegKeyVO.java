package kr.gooroom.gpms.valid.service;

import java.io.Serializable;
import java.util.Date;

@SuppressWarnings("serial")
public class ClientRegKeyVO implements Serializable {

	private String regKeyNo;

	private Date validDate;
	private Date expireDate;

	private Date modDate;
	private String modUserId;

	private String ipRange;

	public ClientRegKeyVO() {
	}

	public Date getValidDate() {
		return validDate;
	}

	public void setValidDate(Date validDate) {
		this.validDate = validDate;
	}

	public Date getExpireDate() {
		return expireDate;
	}

	public void setExpireDate(Date expireDate) {
		this.expireDate = expireDate;
	}

	public Date getModDate() {
		return modDate;
	}

	public void setModDate(Date modDate) {
		this.modDate = modDate;
	}

	public String getModUserId() {
		return modUserId;
	}

	public void setModUserId(String modUserId) {
		this.modUserId = modUserId;
	}

	public String getIpRange() {
		return ipRange;
	}

	public void setIpRange(String ipRange) {
		this.ipRange = ipRange;
	}

	public String getRegKeyNo() {
		return regKeyNo;
	}

	public void setRegKeyNo(String regKeyNo) {
		this.regKeyNo = regKeyNo;
	}

}
